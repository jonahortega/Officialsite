// Helper function to get Greek organizations for a university
export const getGreekOrganizations = (universityName) => {
  return greekLifeData[universityName] || { fraternities: [], sororities: [] };
};

// Helper function to get all fraternities for a university
export const getFraternities = (universityName) => {
  const orgs = getGreekOrganizations(universityName);
  return orgs.fraternities || [];
};

// Helper function to get all sororities for a university
export const getSororities = (universityName) => {
  const orgs = getGreekOrganizations(universityName);
  return orgs.sororities || [];
};

// Helper function to search Greek organizations
export const searchGreekOrganizations = (universityName, searchTerm) => {
  const orgs = getGreekOrganizations(universityName);
  const allOrgs = [...orgs.fraternities, ...orgs.sororities];
  
  if (!searchTerm) return allOrgs;
  
  return allOrgs.filter(org => 
    org.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
    org.letters.toLowerCase().includes(searchTerm.toLowerCase())
  );
};
